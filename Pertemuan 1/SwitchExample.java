public class SwitchExample 
{
	public static void main(String[] args) 
	{ 
		int number=20;

		switch(number){
		case 10: System.out.println("10");break;
		case 20: System.out.println("20");break;
		case 30: System.out.println("30");break; 
		default:System.out.println("Not in 10, 20 or 30"); 
		}
	}
}


/*
	program diatas merupakan contoh pemakaian switch case yang dimana hanya akan ada satu kondisi saja yang akan dijalnkan sessuai dengan kondisi yang ada pada case 
	pada program diatas terdapat data integer dengan variabel number=20
	-pada line 9 terdapat nilai yang sama dengan nilai number(20) yaitu case 20 sehingga akan mengahsilkan output "20" dan melakukan break 
 


*/