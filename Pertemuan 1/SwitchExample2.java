public class SwitchExample2 {
	public static void main(String[] args){ 
		int number=20;

		switch(number){
		    case 10: System.out.println("10");
		    case 20: System.out.println("20");
		    case 30: System.out.println("30"); 
		    default: System.out.println("Not in 10, 20 or 30"); 
		}
	}
}

/*
	program diatas merupakan contoh pemakaian switch case yang sama seperti switch case sebelumnya
	hanya saja pada program ini setiap case tidak memakai perintah break sehingga membuat semua case akan di tampilkan.       




*/